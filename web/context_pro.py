def get_lang_code(lang_text: str):
    return [k for k, v in languages.items() if v == lang_text][0]


def base(request):
    lang = Settings().get(key='lang_code', tp=str)
    if lang is "":
        lang = "uz"
    baseL[lang].update(dashboard[lang])
    baseL[lang].update(pickup_order[lang])
    baseL[lang].update(worker[lang])
    baseL[lang].update(product[lang])
    baseL[lang].update(tables[lang])
    baseL[lang].update(expense[lang])
    baseL[lang].update(trad[lang])
    baseL[lang].update(archive[lang])
    baseL[lang].update(signin[lang])
    baseL[lang].update(waiter[lang])
    baseL[lang].update(settings_window[lang])
    baseL[lang].update(receipt_info[lang])
    context = baseL[lang]
    return context


def _(key: str):
    context = base("request")
    return context[key]
